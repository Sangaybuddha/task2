class Dog:
    def __init__(self, name, colour, age):
        self.name = name
        self.colour = colour
        self.age = age
dog1 = Dog("Dawa", "brown", 2)
dog2 = Dog("Nima", "grey", 4) 
dog3 = Dog("Blacky", "black", 5)
print(dog1.name)
print(dog1.colour)
print(dog1.age)
print(dog2.name)
print(dog2.colour)
print(dog2.age)
print(dog3.name)
print(dog3.colour)
print(dog3.age)